    <h1 class="title">
        Reconciliation
        <p><em>fabrication of a good memory</em></p>
    </h1>
    <div class="poem">
        <div class="poemtext"
            > On nights that crawl
            I say to you
            Bless the hearts 
            That linger in the faintest corners
            Of the mind
            
            Bless the place
            Where land meets sea
            And all night fades
            Into black bitterness
            Whose currents well up inside 
            
            When all you can do
            Is sit by the dock 
            And let the water pass
            Between your toes on the shore.
            That is okay
            
            If all you can do is wait
            As the night rolls 
            On the back
            Of the rising tide.
            It's okay
            
            But occasionally you can walk
            Down the dock
            To the precipice 
            Of the grey water
            Beneath your feet
            
            Occasionally 
            You can look
            Down into that pool
            And see a beautiful face
            Lost in the waves
            
            But when the time comes
            You will turn 
            Your attention
            To the distant shimmer of light
            On the horizon
            
            When that time comes 
            You will leap
            Onto the pale cold water
            Wondering if it will hold
            Half wishing
            You will drown

            You might
            Kick your feet
            And swim forward
            Inch by inch
            Consumed
            At every turn
            
            But he who lives righteously
            Shall rise up
            And walk
            On tenuous ground
            Towards the light
            
            He who lives
            Acknowledges what is below
            And still continues to trust
            That it is
            Enough
            
            For sometimes on a day
            Like no other day
            The world has nothing with you
            But to say
            That all is well
        </div>
    </div>
