<h1>Peach Picking</h1>
<body>
    <br> We wrap ourselves
    <br> In light linen coats
    <br> Grey woolen socks
    <br> And leather shoes
    <br> To journey
    <br> With baskets in hand
    <br> To narrow streets
    <br> Of yellow peach trees
    <br> That meander through the wood
    <br> 
    <br> We follow the trail up a hill
    <br> To feel the sky widen
    <br> Before us
    <br> To dance freely
    <br> As it does
    <br> To pause.
    <br> To stitch together
    <br> Shapes, lines, colors 
    <br> Where trees become forest
    <br> Leaves--a canopy
    <br> 
    <br> Dancing swiftly, swiftly
    <br> Down blades of grass 
    <br> Through matchbox meadows
    <br> Patchwork paths
    <br> We are free.
    <br> Weaving in and out 
    <br> Of expansive fabrics
    <br> Crimson bows
    <br> Against the horizon
    <br> Tapping toes
    <br> On the rising sun   
</body>

