<h1>Reconciliation</h1>
<p><em>fabrication of a good memory</em></p>
<body>
    <br> On nights
    <br> That crawl
    <br> I say to you
    <br> Bless the hearts 
    <br> That linger in the faintest corners
    <br> Of the mind
    <br> 
    <br> Bless the place
    <br> Where land meets sea
    <br> And all night fades
    <br> Into black bitterness
    <br> Whose currents 
    <br> Well up inside 
    <br> 
    <br> When all you can do
    <br> Is sit by the dock 
    <br> And let the water pass
    <br> Between your toes
    <br> On the shore.
    <br> That is okay
    <br> 
    <br> If all you can do 
    <br> Is wait
    <br> As the night rolls 
    <br> On the back
    <br> Of the rising tide.
    <br> It's okay 
    <br> 
    <br> But occasionally 
    <br> You can walk
    <br> Down the dock
    <br> To the precipice 
    <br> Of the grey water
    <br> Beneath your feet
    <br> 
    <br> Occasionally
    <br> You can look
    <br> Down into that pool
    <br> And see a
    <br> Beautiful face
    <br> Lost in the waves
    <br> 
    <br> <br> But when the time comes
    <br> You will turn 
    <br> Your attention
    <br> To the distant
    <br> Shimmer of light
    <br> On the horizon
    <br> 
    <br> When that time comes 
    <br> You will leap
    <br> Onto the pale cold water
    <br> Wondering if it will hold
    <br> Half wishing
    <br> You will drown
    <br> 
    <br> You might
    <br> Kick your feet
    <br> And swim forward
    <br> Inch by inch
    <br> Consumed
    <br> At every turn
    <br> 
    <br> But he who lives
    <br> Righteously
    <br> Shall rise up
    <br> And walk
    <br> On tenuous ground
    <br> Towards the light
    <br> 
    <br> He who lives
    <br> Acknowledges what is below
    <br> And still
    <br> Continues to trust
    <br> That it is
    <br> Enough
    <br> 
    <br> For sometimes
    <br> On a day like 
    <br> No other day
    <br> The world has nothing
    <br> With you but to say
    <br> That all is well
</body>
